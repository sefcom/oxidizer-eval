def run(cmd: str, cwd=None, capture_output=True) -> None:
    """Run a shell command, exit if it fails."""
    result = subprocess.run(cmd.split(), cwd=cwd, capture_output=capture_output, text=True)
    if result.returncode != 0:
        raise subprocess.CalledProcessError(result.returncode, cmd, output=result.stdout, stderr=result.stderr)


def compile_stdlib_binary(toolchain, opt_level):
    """
    Compile stdlib_template.rs using cargo to generate binary with debug symbols.
    """
    binary_name = f"{NIGHTLY_TO_STABLE[toolchain]}-O{opt_level}"
    binary_path = TARGET_BINARIES_DIR / binary_name

    if binary_path.exists():
        logger.info(f"Binary for {toolchain} with O{opt_level} already exists, skipping build.")
        return

    TARGET_BINARIES_DIR.mkdir(parents=True, exist_ok=True)

    # Set rustflags for debug info
    stable_version = NIGHTLY_TO_STABLE[toolchain]
    version_parts = tuple(int(x) for x in stable_version.split("."))

    rustflags = ""
    if version_parts >= (1, 65, 0):
        rustflags = "-Z inline-llvm=off"

    env = os.environ.copy()
    if rustflags:
        env["RUSTFLAGS"] = rustflags

    with tempfile.TemporaryDirectory(prefix="rust_stdlib_build_") as tmpdir:
        project_dir = Path(tmpdir) / "stdlib_project"

        # Create project with cargo new
        run(f"rustup run {toolchain} cargo new {project_dir}")

        # Copy stdlib_template.rs as main.rs
        import shutil

        shutil.copy(STDLIB_TEMPLATE, project_dir / "src" / "main.rs")

        # Write release profile to Cargo.toml
        cargo_toml = project_dir / "Cargo.toml"
        with open(cargo_toml, "a") as f:
            f.write("\n[profile.release]\n")
            f.write("debug = 2\n")  # Full debug info
            if opt_level in ("s", "z"):
                f.write(f'opt-level = "{opt_level}"\n')
            else:
                f.write(f"opt-level = {opt_level}\n")

        # Build with cargo
        build_cmd = [
            "rustup",
            "run",
            toolchain,
            "cargo",
            "build",
            "--release",
            "--target",
            "x86_64-unknown-linux-gnu",
        ]

        result = subprocess.run(build_cmd, cwd=project_dir, env=env, capture_output=True, text=True)
        if result.returncode != 0:
            raise subprocess.CalledProcessError(
                result.returncode, " ".join(build_cmd), output=result.stdout, stderr=result.stderr
            )

        # Copy the compiled binary to target directory
        compiled_binary = project_dir / "target" / "x86_64-unknown-linux-gnu" / "release" / "stdlib_project"
        if not compiled_binary.exists():
            raise RuntimeError(f"Compiled binary not found at {compiled_binary}")

        shutil.copy(compiled_binary, binary_path)


def generate_flirt_sig(binary_file):
    """Generate FLIRT signature for a binary file using parse_sig.py."""
    binary_path = TARGET_BINARIES_DIR / binary_file
    sig_path = TARGET_FLIRT_SIGS_DIR / f"{binary_file}.sig"

    if sig_path.exists():
        logger.info(f"Signature for {binary_file} already exists, skipping.")
        return

    if not binary_path.exists():
        logger.warning(f"Binary not found: {binary_path}")
        return

    TARGET_FLIRT_SIGS_DIR.mkdir(parents=True, exist_ok=True)

    logger.info(f"Generating signature for {binary_file}...")

    # Use parse_sig.py to generate signature
    if not parse_sig(binary_path, sig_path):
        raise RuntimeError(f"Failed to generate signature for {binary_file}")

    pat_path = TARGET_BINARIES_DIR / f"{binary_file}.pat"
    pat_path.unlink(missing_ok=True)

    logger.info(f"Successfully generated signature for {binary_file}")


def generate_type_db(binary_file: Path, output_json: Path) -> None:
    """Generate type DB by parsing binary file with dwarf_parser."""
    if not binary_file.exists():
        logger.warning(f"Binary not found: {binary_file}")
        return

    with tempfile.TemporaryDirectory(prefix="type_db_") as tmpdir:
        tmpdir_path = Path(tmpdir)
        temp_json = tmpdir_path / "output.json"

        try:
            result = subprocess.run(
                [str(DWARF_PARSER_BIN), str(binary_file), str(temp_json)], capture_output=True, text=True
            )
            if result.returncode != 0:
                logger.warning(f"dwarf_parser failed for {binary_file.name}: {result.stderr}")
                return
        except Exception as e:
            logger.warning(f"dwarf_parser failed for {binary_file.name}: {e}")
            return

        if not temp_json.exists():
            logger.warning(f"dwarf_parser did not generate output for {binary_file.name}")
            return

        try:
            with open(temp_json, "r") as f:
                data = json.load(f)
        except Exception as e:
            logger.warning(f"Failed to load JSON for {binary_file.name}: {e}")
            return

        functions = data.get("functions", [])
        structs = data.get("structs", {})

        # Clean up function data
        for v in functions:
            if "decl_path" in v:
                del v["decl_path"]
            if "addr" in v:
                del v["addr"]
            if "local_variables" in v:
                del v["local_variables"]

        merged_structs = list(structs.values()) if isinstance(structs, dict) else structs

        output_json.parent.mkdir(parents=True, exist_ok=True)
        with open(output_json, "w") as f:
            json.dump({"functions": functions, "structs": merged_structs}, f, separators=(",", ":"))


def install_toolchains(logger):
    """Install all required Rust toolchains and rust-src component."""
    unique_toolchains = list(NIGHTLY_TO_STABLE.keys())
    logger.info(f"Installing {len(unique_toolchains)} toolchains...")

    for i, toolchain in enumerate(unique_toolchains, 1):
        try:
            logger.info(f"[{i}/{len(unique_toolchains)}] Installing {toolchain}...")
            run(f"rustup install {toolchain} --profile minimal")
            run(f"rustup component add rust-src --toolchain {toolchain}")
        except subprocess.CalledProcessError as e:
            logger.warning(f"Failed to install {toolchain}: {e.stderr if e.stderr else str(e)}")
        except Exception as e:
            logger.warning(f"Error installing {toolchain}: {e}")

    logger.info("Toolchain installation complete.\n")


if __name__ == "__main__":
    # Setup logging
    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s - %(levelname)s - %(message)s",
        handlers=[logging.FileHandler("gen_stdlib_db.log", mode="w"), logging.StreamHandler()],
    )
    logger = logging.getLogger(__name__)

    # Install all required toolchains first
    # install_toolchains(logger)

    opt_levels = ["0", "1", "2", "3", "s", "z"]

    # Build list of all tasks
    tasks = []
    for toolchain in NIGHTLY_TO_STABLE:
        for opt_level in opt_levels:
            tasks.append((toolchain, opt_level))

    total = len(tasks)
    failed_builds = []
    successful = 0

    logger.info(f"Starting {total} compilations...")

    with ProcessPoolExecutor(max_workers=20) as executor:
        futures = {executor.submit(compile_stdlib_binary, tc, opt): (tc, opt) for tc, opt in tasks}

        for future in as_completed(futures):
            toolchain, opt_level = futures[future]
            try:
                future.result()
                successful += 1
                logger.info(f"[{successful + len(failed_builds)}/{total}] ✓ {toolchain} O{opt_level}")
            except subprocess.CalledProcessError as e:
                failed_builds.append((toolchain, opt_level, str(e)))
                logger.error(f"[{successful + len(failed_builds)}/{total}] ✗ {toolchain} O{opt_level}: {e}")
                if e.output:
                    logger.error(f"stdout: {e.output}")
                if e.stderr:
                    logger.error(f"stderr: {e.stderr}")
            except Exception as e:
                failed_builds.append((toolchain, opt_level, str(e)))
                logger.error(f"[{successful + len(failed_builds)}/{total}] ✗ {toolchain} O{opt_level}: {e}")
                logger.error(traceback.format_exc())

    logger.info(f"{'='*50}")
    logger.info(f"Compilation complete: {successful} succeeded, {len(failed_builds)} failed")

    if failed_builds:
        logger.info(f"Failed compilations:")
        for toolchain, opt_level, error in failed_builds:
            logger.info(f"  - {toolchain} O{opt_level}: {error}")

    # Generate FLIRT signatures in parallel
    logger.info(f"\n{'='*50}")
    logger.info("Starting signature generation...")

    TARGET_FLIRT_SIGS_DIR.mkdir(parents=True, exist_ok=True)
    binary_files = [
        f
        for f in os.listdir(TARGET_BINARIES_DIR)
        if os.path.isfile(TARGET_BINARIES_DIR / f) and not f.endswith(".i64") and not f.endswith(".pat")
    ]

    sig_total = len(binary_files)
    sig_failed = []
    sig_successful = 0

    with ProcessPoolExecutor(max_workers=20) as executor:
        sig_futures = {executor.submit(generate_flirt_sig, f): f for f in binary_files}

        for future in as_completed(sig_futures):
            binary_file = sig_futures[future]
            try:
                future.result()
                sig_successful += 1
                logger.info(f"[{sig_successful + len(sig_failed)}/{sig_total}] ✓ {binary_file}")
            except Exception as e:
                sig_failed.append((binary_file, str(e)))
                logger.error(f"[{sig_successful + len(sig_failed)}/{sig_total}] ✗ {binary_file}: {e}")
                logger.error(traceback.format_exc())

    logger.info(f"\n{'='*50}")
    logger.info(f"Signature generation complete: {sig_successful} succeeded, {len(sig_failed)} failed")

    if sig_failed:
        logger.info(f"Failed signatures:")
        for binary_file, error in sig_failed:
            logger.info(f"  - {binary_file}: {error}")

    # Generate type DB from binaries
    logger.info(f"\n{'='*50}")
    logger.info("Starting type DB generation...")

    TARGET_TYPE_DB_DIR.mkdir(parents=True, exist_ok=True)
    run("cargo build --release", cwd=DWARF_PARSER_DIR)

    type_db_failed = []
    type_db_successful = 0

    for version in RUST_VERSIONS:
        binary_file = TARGET_BINARIES_DIR / f"{version}-O3"
        type_db_file = TARGET_TYPE_DB_DIR / f"{version}.json"
        if type_db_file.exists():
            logger.info(f"Type DB for {version} already exists, skipping.")
            type_db_successful += 1
            continue
        try:
            logger.info(f"Generating type DB for {version}...")
            generate_type_db(binary_file, type_db_file)
            type_db_successful += 1
            logger.info(f"[{type_db_successful + len(type_db_failed)}/{len(RUST_VERSIONS)}] ✓ {version}")
        except Exception as e:
            type_db_failed.append((version, str(e)))
            logger.error(f"[{type_db_successful + len(type_db_failed)}/{len(RUST_VERSIONS)}] ✗ {version}: {e}")
            logger.error(traceback.format_exc())

    logger.info(f"\n{'='*50}")
    logger.info(f"Type DB generation complete: {type_db_successful} succeeded, {len(type_db_failed)} failed")

    if type_db_failed:
        logger.info(f"Failed type DBs:")
        for version, error in type_db_failed:
            logger.info(f"  - {version}: {error}")
